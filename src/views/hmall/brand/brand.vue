<template>
    <div class="page-body B2b">
        <div class="page-body-cont">
            <div class="query-cont__row">
                <div class="query-cont__col">
                    <div class="query-col__lable">品牌编码：</div>
                    <div class="query-col__input">
                        <el-input type="text" v-model="queryParams.code" maxlength="50" placeholder="请输入品牌编码"></el-input>
                    </div>
                </div>
                <div class="query-cont__col">
                    <div class="query-col__lable">品牌名称：</div>
                    <div class="query-col__input">
                        <el-input type="text" v-model="queryParams.name" maxlength="50" placeholder="请输入品牌名称"></el-input>
                    </div>
                </div>
                <div class="query-cont__col">
                    <div class="query-col__lable">品牌状态：</div>
                    <div class="query-col__input">
                        <el-select v-model="queryParams.status" style="width: 100%">
                            <el-option v-for="item in brandStatusOptions" :key="item.value" :label="item.label" :value="item.value">
                            </el-option>
                        </el-select>
                    </div>
                </div>
                <div class="query-cont__col">
                    <div class="query-col__lable">维护人：</div>
                    <div class="query-col__input">
                        <el-input type="text" v-model="queryParams.operator" maxlength="50" placeholder="请输入维护人姓名"></el-input>
                    </div>
                </div>
                <div class="query-cont__col">
                    <h-button type="primary" @click="onQuery()">
                        查询
                    </h-button>
                    <h-button @click="onReset()">重置</h-button>
                </div>
            </div>
            <brandTable ref="baseTable" :tableData="tableData" :paginationData="paginationData" @updateStatus="onQuery" @updateBrand="updateBrandChange" @onSizeChange="onSizeChange" @onCurrentChange="onCurrentChange" @openMark="openMark">
            </brandTable>
        </div>
        <el-dialog width="700px" title="品牌编辑" :visible.sync="dialogBrandEdit" :close-on-click-modal="false">
            <el-form :model="form" :rules="rules" ref="form" :label-width="formLabelWidth">
                <el-form-item label="品牌编号" v-if="this.status === 'modify'">
                    {{form.code}}
                </el-form-item>
                <el-form-item prop="name" label="品牌名称">
                    <el-input v-model="form.name" maxlength="10" style="width: 300px" placeholder="请输入品牌中文名称"></el-input>
                </el-form-item>
                <el-form-item prop="englishName" label="英文名称">
                    <el-input v-model="form.englishName" placeholder="请输入英文名称" maxlength="25" style="width: 300px"></el-input>
                </el-form-item>
                <el-form-item prop="logoUrl" label="品牌logo">
                    <!--logoUrl-->
                    <SingleUpload :upload="uploadInfo" :imageUrl="imageUrl" ref="uploadImg" @back-event="readUrl" />
                    <div class="upload-tips">
                        尺寸300x300,2m以内，支持jpg、jpeg、png
                    </div>
                </el-form-item>
                <el-form-item prop="description" label="品牌描述">
                    <el-input type="textarea" maxlength="100" placeholder="100字以内" v-model="form.description" style="width: 300px;" :rows="5"></el-input>
                </el-form-item>
            </el-form>
            <div slot="footer" class="dialog-footer">
                <h-button @click="dialogBrandEdit = false">取 消</h-button>
                <h-button type="primary" @click="submitForm('form')" :loading="isSaving">保 存</h-button>
            </div>
        </el-dialog>
    </div>
</template>

<script>
import brandTable from './components/brandTable'
import { mapState, mapActions } from 'vuex'
import { interfaceUrl } from '@/api/config'
import { createBrand, updateBrand } from './api/index'
import { BRAND_STATUS } from './const'
import { IsChinese } from '@/rules'
export default {
    name: 'brand',
    components: {
        brandTable
    },
    data () {
        return {
            initParams: {},
            queryParams: {
                name: '',
                code: '',
                pageNumber: 1,
                pageSize: 10,
                operator: '',
                status: ''
            },
            subsectionCodeList: [],
            tableData: [],
            paginationData: {},
            dialogBrandEdit: false,
            form: {
                name: '',
                englishName: '',
                id: '',
                logoUrl: '',
                description: ''
            },
            tempForm: {},
            formLabelWidth: '150px',
            modifyId: 0,
            status: 'add',
            rules: {
                name: [
                    { required: true, whitespace: true, message: '请输入品牌中文名称', trigger: 'blur' }
                ],
                englishName: [
                    { required: false, whitespace: true },
                    { validator: IsChinese, trigger: 'blur', whitespace: true }
                ],
                logoUrl: [
                    { required: true, message: '请上传品牌logo' }
                ]
            },
            brandStatusOptions: BRAND_STATUS,
            isSaving: true
        }
    },
    computed: {
        ...mapState('brand', {
            brandListInfo: 'brandListInfo',
            brandDetail: 'brandDetail'
        }),
        uploadInfo () {
            return {
                action: interfaceUrl + 'tms/files/upload',
                data: {
                    updateUid: this.userInfo.employeeName
                },
                accept: 'image/jpeg, image/jpg, image/png'
            }
        },
        imageUrl () {
            return this.form.logoUrl
        },
        ...mapState({
            userInfo: state => state.userInfo
        })
    },
    methods: {
        ...mapActions('brand', [
            'findBrandList',
            'findBrandArea',
            'findBrandDetail'
        ]),
        onReset () {
            this.queryParams = { ...this.initParams }
            this.paginationData = {
                pageNumber: 1,
                pageSize: 10
            }
            this.onQuery()
        },
        updateBrandChange (col) {
            this.modifyId = col.id
            this.findBrandDetailAsync()
            this.isSaving = false
        },
        submitForm (formName) {
            if (this.isSaving) {
                return
            }
            this.isSaving = true
            this.$refs[formName].validate(async (valid) => {
                if (valid) {
                    try {
                        await this.saveBrand()
                        this.isSaving = false
                    } catch (e) {
                        this.isSaving = false
                    }
                } else {
                    this.isSaving = false
                }
            })
        },
        async saveBrand () {
            let { ...params } = this.form
            params.operator = this.userInfo.employeeName
            if (this.status === 'add') {
                await createBrand(params)
            } else if (this.status === 'modify') {
                params.id = this.modifyId
                await updateBrand(params)
            }
            this.dialogBrandEdit = false
            this.$message({
                message: this.status === 'add' ? '品牌添加成功！' : '品牌修改成功！',
                type: 'success'
            })
            this.onQuery()
        },
        // 图片上传成功回调
        readUrl (val) {
            this.form.logoUrl = val.imageUrl
        },
        async findBrandDetailAsync () {
            await this.findBrandDetail(this.modifyId)
            this.form = {
                code: this.brandDetail.code,
                name: this.brandDetail.name,
                englishName: this.brandDetail.englishName,
                logoUrl: this.brandDetail.logoUrl,
                description: this.brandDetail.description
            }
            this.status = 'modify'
            this.dialogBrandEdit = true
            this.$nextTick(() => {
                this.$refs.form.clearValidate()
            })
        },
        async onQuery () {
            const { ...params } = this.queryParams
            await this.findBrandList(params)
            this.tableData = this.brandListInfo.records
            this.paginationData = {
                pageNumber: this.brandListInfo.current,
                pageSize: this.brandListInfo.size,
                totalElements: this.brandListInfo.total
            }
        },
        openMark (status) {
            let { ...form } = this.tempForm
            this.form = form
            this.status = status
            this.dialogBrandEdit = true
            this.$nextTick(function () {
                this.$refs.form.clearValidate()
            })
            this.isSaving = false
        },
        onSizeChange (val) {
            this.queryParams.pageSize = val
            this.onQuery()
        },
        onCurrentChange (val) {
            this.queryParams.pageNumber = val
            this.onQuery()
        }
    },
    mounted () {
        this.initParams = { ...this.queryParams }
        this.onQuery()
        let { ...form } = this.form
        // 用于清空弹出层的form数据
        this.tempForm = form
    }
}
</script>

